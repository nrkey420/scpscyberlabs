<#
.SYNOPSIS
    Deploy Lab 03 — SOC Analyst Training Environment

.DESCRIPTION
    Orchestrates the full deployment of the SOC Analyst lab. Students learn
    to use Security Onion and Splunk to detect, triage, and respond to
    simulated attack events generated by scheduled tasks on Windows endpoints.

    Each student gets three per-student VMs connected to their own segment,
    all forwarding logs to two shared SIEM/NSM platforms (one set per class).

    VMs Deployed
    ------------
    Per Student (up to 15 students):
      - Windows 10 Workstation  (endpoints generating telemetry, Splunk UF installed)
      - Windows Server 2019 AD  (domain controller, Splunk UF installed)
      - Ubuntu Linux Web Server (rsyslog forwarding to Security Onion + Splunk)

    Per Class (shared):
      - Security Onion 2.4      (IDS/NSM — receives syslog from Linux VMs)
      - Splunk Enterprise 9.1   (SIEM — receives Windows UF + Linux syslog on port 9997)

    Network Topology (ClassId=1, StudentId=4 example)
    --------------------------------------------------

    soc-net-C1-S4 (Private)
      |          |         |
    [Win10 .20] [AD .21] [Web .30]
         \          \       /
          \--- log forwarding --→ shared-soc-net-C1
                                     |         |
                               [SecOnion .50] [Splunk .51]

    Attack Simulation (fires 30 min after deploy)
    -----------------------------------------------
    Scheduled tasks on Windows VMs simulate:
      - Failed logon attempts (Event ID 4625)
      - PowerShell encoded command execution (Event ID 4104)
      - New service installation (Event ID 7045)
      - User account creation (Event ID 4720)

    Per-student Splunk roles allow access to:
      - index=windows  (Windows event logs)
      - index=cyberlab (custom app index)

    Timing Estimates
    ----------------
    - Prerequisites / switch creation  :  ~30 seconds
    - Shared VM deployment             :  ~8-12 minutes
    - Per-student parallel deploy      :  ~10-15 minutes (ThrottleLimit=5)
    - Total (15 students)              :  ~25-35 minutes

.PARAMETER SessionId
    GUID identifying this class session.

.PARAMETER ClassId
    1 or 2. Maps to the second IP octet.

.PARAMETER StudentIds
    Array of student identifiers (1-15 entries).

.PARAMETER TimeoutMinutes
    Maximum deployment time in minutes. Default 120.

.EXAMPLE
    .\Deploy-03-SOCAnalyst.ps1 `
        -SessionId ([guid]::NewGuid()) `
        -ClassId 1 `
        -StudentIds @('alice','bob','carol','dave')

.NOTES
    Author  : SCPS CyberLab Orchestration System
    Lab     : 03 — SOC Analyst
    Version : 1.0.0
    Requires: Windows Server 2022, Hyper-V, Administrator rights,
              OpenSSH/sshpass on host for Linux VM configuration
#>
