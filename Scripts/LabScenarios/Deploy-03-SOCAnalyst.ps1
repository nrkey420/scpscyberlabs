$studentJobs = $studentArgs | ForEach-Object -ThrottleLimit 5 -Parallel {
    $sa         = $_
    $sid        = $sa.StudentNum
    $student    = $sa.StudentId
    $cid        = $sa.ClassId
    $sess       = $sa.SessionId
    $short      = $sa.ShortId
    $tplPath    = $sa.TemplatePath
    $vmBase     = $sa.VMBasePath
    $tpls       = $sa.Templates
    $splunkIp   = $sa.SplunkIp
    $secOnionIp = $sa.SecOnionIp

    $result = @{ StudentId=$student; StudentNum=$sid; Success=$false; Error=''; Credentials=@{}; VMNames=@() }

    try {
        $studentDir = Join-Path $vmBase "${sess}\${student}"
        if (-not (Test-Path $studentDir)) { New-Item -ItemType Directory -Path $studentDir -Force | Out-Null }

        $ipBase     = "10.${cid}.${sid}"
        $switchName = "soc-net-C${cid}-S${sid}"
        $sharedSwitch = "shared-soc-net-C${cid}"

        $names = @{
            Win10  = "Lab03-Win10-C${cid}-S${sid}-${short}"
            WinAD  = "Lab03-WinAD-C${cid}-S${sid}-${short}"
            WebSrv = "Lab03-Web-C${cid}-S${sid}-${short}"
        }

        # Generate credentials
        $chars = 'abcdefghijkmnopqrstuvwxyzABCDEFGHJKLMNPQRSTUVWXYZ23456789!@#$%&*'
        function GenPwd {
            $rng = [System.Security.Cryptography.RandomNumberGenerator]::Create()
            $b   = [byte[]]::new(16); $rng.GetBytes($b)
            return -join ($b | ForEach-Object { $chars[$_ % $chars.Length] })
        }
        $creds = @{
            Win10Admin    = GenPwd
            Win10Student  = GenPwd
            WinADAdmin    = GenPwd
            WinADStudent  = GenPwd
            WebSrvAdmin   = GenPwd
        }

        # Differencing disks
        $diskMap = @{
            Win10  = @{ Template = 'windows-10-enterprise.vhdx';   Mem=4096; CPU=2; SB=$true  }
            WinAD  = @{ Template = 'windows-server-2019-ad.vhdx';  Mem=4096; CPU=2; SB=$true  }
            WebSrv = @{ Template = 'ubuntu-server-22.04-web.vhdx'; Mem=2048; CPU=1; SB=$false }
        }

        $diskPaths = @{}
        foreach ($role in $diskMap.Keys) {
            $parent = Join-Path $tplPath $diskMap[$role].Template
            $child  = Join-Path $studentDir "$($names[$role]).vhdx"
            New-VHD -Path $child -ParentPath $parent -Differencing -ErrorAction Stop | Out-Null
            $diskPaths[$role] = $child
        }

        # Create VMs — student VMs connect to both their segment AND the shared SOC switch
        foreach ($role in $diskMap.Keys) {
            $vmName   = $names[$role]
            $def      = $diskMap[$role]
            $existing = Get-VM -Name $vmName -ErrorAction SilentlyContinue
            if ($existing) {
                Stop-VM -Name $vmName -TurnOff -Force -ErrorAction SilentlyContinue
                Remove-VM -Name $vmName -Force -ErrorAction SilentlyContinue
            }
            $vm = New-VM -Name $vmName -MemoryStartupBytes ([int64]$def.Mem * 1MB) `
                         -VHDPath $diskPaths[$role] -SwitchName $switchName `
                         -Generation 2 -ErrorAction Stop
            Set-VM -Name $vmName -ProcessorCount $def.CPU -DynamicMemory:$false -ErrorAction Stop
            # Add second NIC on shared SOC net for log forwarding
            Add-VMNetworkAdapter -VMName $vmName -SwitchName $sharedSwitch -ErrorAction Stop
            Set-VMFirmware -VMName $vmName -EnableSecureBoot:$def.SB -ErrorAction SilentlyContinue
            $result.VMNames += $vmName
        }

        # Start all three VMs
        Start-VM -Name $names.Win10  -ErrorAction Stop
        Start-VM -Name $names.WinAD  -ErrorAction Stop
        Start-VM -Name $names.WebSrv -ErrorAction Stop

        # Wait for Windows VMs
        foreach ($winRole in @('Win10','WinAD')) {
            $elapsed = 0
            while ($elapsed -lt 240) {
                $hb = (Get-VM -Name $names[$winRole] -ErrorAction SilentlyContinue).Heartbeat
                if ($hb -in @('OkApplicationsHealthy','OkApplicationsUnknown')) { break }
                Start-Sleep -Seconds 5; $elapsed += 5
            }
        }

        # Configure Windows 10 via PowerShell Direct
        $win10Cred = [PSCredential]::new('Administrator',
            (ConvertTo-SecureString 'Enterprise123!' -AsPlainText -Force))

        Invoke-Command -VMName $names.Win10 -Credential $win10Cred -ScriptBlock {
            param($ip, $gw, $splunkTarget, $adminPwd, $studentUser, $studentPwd)
            # Static IP on first adapter (student segment)
            $adap = Get-NetAdapter | Where-Object Status -eq 'Up' | Select-Object -First 1
            if ($adap) {
                Get-NetIPAddress -InterfaceIndex $adap.ifIndex -AddressFamily IPv4 -ErrorAction SilentlyContinue |
                    Remove-NetIPAddress -Confirm:$false -ErrorAction SilentlyContinue
                New-NetIPAddress -InterfaceIndex $adap.ifIndex -IPAddress $ip -PrefixLength 24 -DefaultGateway $gw -ErrorAction SilentlyContinue
            }
            # Set passwords
            Set-LocalUser -Name 'Administrator' -Password (ConvertTo-SecureString $adminPwd -AsPlainText -Force) -ErrorAction SilentlyContinue
            $sp = ConvertTo-SecureString $studentPwd -AsPlainText -Force
            New-LocalUser -Name $studentUser -Password $sp -FullName 'SOC Student' -PasswordNeverExpires -ErrorAction SilentlyContinue
            Add-LocalGroupMember -Group 'Administrators' -Member $studentUser -ErrorAction SilentlyContinue
            # Configure Splunk UF outputs.conf
            $ufPath = 'C:\Program Files\SplunkUniversalForwarder\etc\system\local'
            if (Test-Path $ufPath) {
                $conf = "[tcpout]`ndefaultGroup = cyberlab_indexer`n`n[tcpout:cyberlab_indexer]`nserver = ${splunkTarget}:9997"
                $conf | Set-Content -Path "$ufPath\outputs.conf" -Encoding UTF8
                Restart-Service SplunkForwarder -ErrorAction SilentlyContinue
            }
            # Enhanced audit policy
            auditpol /set /subcategory:"Logon" /success:enable /failure:enable 2>&1 | Out-Null
            auditpol /set /subcategory:"Process Creation" /success:enable /failure:enable 2>&1 | Out-Null
            $pshPath = 'HKLM:\SOFTWARE\Policies\Microsoft\Windows\PowerShell\ScriptBlockLogging'
            if (-not (Test-Path $pshPath)) { New-Item -Path $pshPath -Force | Out-Null }
            Set-ItemProperty -Path $pshPath -Name 'EnableScriptBlockLogging' -Value 1 -Type DWord -ErrorAction SilentlyContinue
        } -ArgumentList "${ipBase}.20", "${ipBase}.1", $splunkIp,
                         $creds.Win10Admin, "socstudent${sid}", $creds.Win10Student -ErrorAction SilentlyContinue

        # Configure Windows AD via PowerShell Direct
        $winADCred = [PSCredential]::new('Administrator',
            (ConvertTo-SecureString 'LabAdmin123!' -AsPlainText -Force))

        Invoke-Command -VMName $names.WinAD -Credential $winADCred -ScriptBlock {
            param($ip, $gw, $splunkTarget, $adminPwd, $studentUser, $studentPwd)
            $adap = Get-NetAdapter | Where-Object Status -eq 'Up' | Select-Object -First 1
            if ($adap) {
                Get-NetIPAddress -InterfaceIndex $adap.ifIndex -AddressFamily IPv4 -ErrorAction SilentlyContinue |
                    Remove-NetIPAddress -Confirm:$false -ErrorAction SilentlyContinue
                New-NetIPAddress -InterfaceIndex $adap.ifIndex -IPAddress $ip -PrefixLength 24 -DefaultGateway $gw -ErrorAction SilentlyContinue
                Set-DnsClientServerAddress -InterfaceIndex $adap.ifIndex -ServerAddresses '127.0.0.1' -ErrorAction SilentlyContinue
            }
            Set-LocalUser -Name 'Administrator' -Password (ConvertTo-SecureString $adminPwd -AsPlainText -Force) -ErrorAction SilentlyContinue
            # Configure Splunk UF
            $ufPath = 'C:\Program Files\SplunkUniversalForwarder\etc\system\local'
            if (Test-Path $ufPath) {
                $conf = "[tcpout]`ndefaultGroup = cyberlab_indexer`n`n[tcpout:cyberlab_indexer]`nserver = ${splunkTarget}:9997"
                $conf | Set-Content -Path "$ufPath\outputs.conf" -Encoding UTF8
                Restart-Service SplunkForwarder -ErrorAction SilentlyContinue
            }
            auditpol /set /subcategory:"Logon" /success:enable /failure:enable 2>&1 | Out-Null
        } -ArgumentList "${ipBase}.21", "${ipBase}.1", $splunkIp,
                         $creds.WinADAdmin, "socstudent${sid}", $creds.WinADStudent -ErrorAction SilentlyContinue

        # Configure Linux Web Server via SSH
        $webIp = "${ipBase}.30"
        Start-Sleep -Seconds 20
        $sshReady = $false
        for ($a = 1; $a -le 8; $a++) {
            $r = & sshpass -p 'labadmin' ssh -o StrictHostKeyChecking=no -o ConnectTimeout=10 "labadmin@${webIp}" 'echo ssh_ok' 2>&1
            if ($r -match 'ssh_ok') { $sshReady = $true; break }
            Start-Sleep -Seconds 10
        }

        if ($sshReady) {
            $netplan = "network:`n  version: 2`n  ethernets:`n    eth0:`n      addresses: [`"${webIp}/24`"]`n      routes:`n        - to: default`n          via: ${ipBase}.1`n      nameservers:`n        addresses: [8.8.8.8]"
            # Configure rsyslog to forward to Security Onion (port 514) and Splunk (port 9997/TCP)
            $rsyslogConf = @"
# Forward to Security Onion
*.* @${secOnionIp}:514
# Forward to Splunk via UDP syslog
*.* @${splunkIp}:514
"@
            $cmds = @(
                "printf '${netplan}' | sudo tee /etc/netplan/50-lab.yaml && sudo netplan apply",
                "sudo hostnamectl set-hostname web-soc-c${cid}-s${sid}",
                "echo 'webadmin:$($creds.WebSrvAdmin)' | sudo chpasswd",
                "sudo useradd -m -s /bin/bash webadmin || true",
                "printf '${rsyslogConf}' | sudo tee /etc/rsyslog.d/99-cyberlab.conf && sudo systemctl restart rsyslog"
            )
            foreach ($cmd in $cmds) {
                & sshpass -p 'labadmin' ssh -o StrictHostKeyChecking=no "labadmin@${webIp}" $cmd 2>&1 | Out-Null
            }
        }

        # Register attack simulation scheduled tasks on Windows VMs
        $win10AttackCred = [PSCredential]::new('Administrator',
            (ConvertTo-SecureString $creds.Win10Admin -AsPlainText -Force))

        Invoke-Command -VMName $names.Win10 -Credential $win10AttackCred -ScriptBlock {
            $triggerTime = (Get-Date).AddMinutes(30)
            $scripts = @{
                'CyberLab-SimFailedLogon' = @'
1..5 | ForEach-Object { net use \\127.0.0.1\IPC$ /user:Administrator WrongPassword123 2>$null }
'@
                'CyberLab-SimEncodedPS' = "powershell.exe -EncodedCommand $([Convert]::ToBase64String([System.Text.Encoding]::Unicode.GetBytes('Write-Host SimulatedEncodedCommand')))"
                'CyberLab-SimUserAcct'  = @'
try { New-LocalUser "svc_tmp_$(Get-Random -Max 9999)" -NoPassword -ErrorAction SilentlyContinue } catch {}
Start-Sleep 5
Get-LocalUser | Where-Object Name -like 'svc_tmp_*' | Remove-LocalUser -ErrorAction SilentlyContinue
'@
            }
            foreach ($taskName in $scripts.Keys) {
                $scriptPath = "C:\Windows\Temp\${taskName}.ps1"
                $scripts[$taskName] | Set-Content -Path $scriptPath -Encoding UTF8 -Force
                try { Unregister-ScheduledTask -TaskName $taskName -Confirm:$false -ErrorAction SilentlyContinue } catch {}
                $action  = New-ScheduledTaskAction -Execute 'powershell.exe' -Argument "-ExecutionPolicy Bypass -NonInteractive -File `"${scriptPath}`""
                $trigger = New-ScheduledTaskTrigger -Once -At $triggerTime
                $sett    = New-ScheduledTaskSettingsSet -AllowStartIfOnBatteries -ExecutionTimeLimit '00:05:00'
                $princ   = New-ScheduledTaskPrincipal -UserId 'SYSTEM' -LogonType ServiceAccount -RunLevel Highest
                Register-ScheduledTask -TaskName $taskName -Action $action -Trigger $trigger -Settings $sett -Principal $princ -Force | Out-Null
            }
        } -ErrorAction SilentlyContinue

        # Create per-student Splunk role via SSH
        $splunkRoleCmds = @(
            "/opt/splunk/bin/splunk add role student_${sid} -auth admin:$(& sshpass -p 'changeme' ssh -o StrictHostKeyChecking=no splunkadmin@${splunkIp} 'cat /opt/splunk/etc/.admin_pass 2>/dev/null || echo changeme') 2>/dev/null || true",
            "/opt/splunk/bin/splunk grant user-role -role student_${sid} -user student_${sid} 2>/dev/null || true"
        )
        # NOTE: Splunk role creation in production should use the REST API or Splunk CLI with correct admin creds
        # The above is a simplified placeholder — instructors should configure roles via the Splunk WebUI post-deploy

        # Create InitialState checkpoints
        foreach ($vmName in $result.VMNames) {
            Checkpoint-VM -Name $vmName -SnapshotName 'InitialState' -ErrorAction SilentlyContinue
        }

        $result.Credentials = @{
            StudentId = $student
            StudentNum = $sid
            Windows10 = @{
                VMName       = $names.Win10
                IPAddress    = "${ipBase}.20"
                Administrator = @{ Username = 'Administrator'; Password = $creds.Win10Admin }
                Student       = @{ Username = "socstudent${sid}"; Password = $creds.Win10Student }
                SplunkUFTarget = "${splunkIp}:9997"
            }
            WindowsAD = @{
                VMName        = $names.WinAD
                IPAddress     = "${ipBase}.21"
                Administrator = @{ Username = 'Administrator'; Password = $creds.WinADAdmin }
                SplunkUFTarget = "${splunkIp}:9997"
            }
            LinuxWebServer = @{
                VMName      = $names.WebSrv
                IPAddress   = $webIp
                webadmin    = @{ Username = 'webadmin'; Password = $creds.WebSrvAdmin }
                RsyslogTarget = "${secOnionIp}:514"
            }
            AttackSimulation = @{
                Note      = 'Scheduled tasks fire 30 minutes after deployment to generate detection events'
                Tasks     = @('CyberLab-SimFailedLogon','CyberLab-SimEncodedPS','CyberLab-SimUserAcct')
                EventIDs  = @(4625, 4104, 4720)
            }
        }

        $result.Success = $true
    } catch {
        $result.Error = $_.ToString()
    }
    return $result
}

