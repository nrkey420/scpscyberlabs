function New-RandomPassword {
    param([int]$Length = 16)
    $chars = 'abcdefghijkmnopqrstuvwxyzABCDEFGHJKLMNPQRSTUVWXYZ23456789!@#$%&*-_=+'
    $rng   = [System.Security.Cryptography.RandomNumberGenerator]::Create()
    $bytes = [byte[]]::new($Length)
    $rng.GetBytes($bytes)
    return -join ($bytes | ForEach-Object { $chars[$_ % $chars.Length] })
}

function Write-Step {
    param([string]$Message, [string]$Color = 'Green')
    Write-Host "[$(Get-Date -Format 'HH:mm:ss')] $Message" -ForegroundColor $Color
}

function New-DifferencingDisk {
    param([Parameter(Mandatory)][string]$ParentPath, [Parameter(Mandatory)][string]$ChildPath)
    if (-not (Test-Path $ParentPath)) { throw "Parent VHDX not found: $ParentPath" }
    $dir = Split-Path $ChildPath -Parent
    if (-not (Test-Path $dir)) { New-Item -ItemType Directory -Path $dir -Force | Out-Null }
    New-VHD -Path $ChildPath -ParentPath $ParentPath -Differencing -ErrorAction Stop | Out-Null
    Write-Step "  Differencing disk: $(Split-Path $ChildPath -Leaf)"
}

function New-LabVM {
    param(
        [Parameter(Mandatory)][string]$VMName,
        [Parameter(Mandatory)][string]$VHDXPath,
        [Parameter(Mandatory)][int]$MemoryMB,
        [Parameter(Mandatory)][int]$vCPU,
        [Parameter(Mandatory)][string[]]$SwitchNames,
        [bool]$SecureBoot = $false
    )
    $existing = Get-VM -Name $VMName -ErrorAction SilentlyContinue
    if ($existing) {
        Stop-VM -Name $VMName -TurnOff -Force -ErrorAction SilentlyContinue
        Remove-VM -Name $VMName -Force -ErrorAction SilentlyContinue
    }
    $vm = New-VM -Name $VMName -MemoryStartupBytes ([int64]$MemoryMB * 1MB) `
                 -VHDPath $VHDXPath -SwitchName $SwitchNames[0] -Generation 2 -ErrorAction Stop
    Set-VM -Name $VMName -ProcessorCount $vCPU -DynamicMemory:$false -ErrorAction Stop
    for ($i = 1; $i -lt $SwitchNames.Count; $i++) {
        Add-VMNetworkAdapter -VMName $VMName -SwitchName $SwitchNames[$i] -ErrorAction Stop
    }
    Set-VMFirmware -VMName $VMName -EnableSecureBoot:$SecureBoot -ErrorAction SilentlyContinue
    Write-Step "  VM created: $VMName ($vCPU vCPU, ${MemoryMB}MB)"
    return $vm
}

function Wait-VMHeartbeat {
    param([Parameter(Mandatory)][string]$VMName, [int]$TimeoutSec = 180)
    $elapsed = 0
    while ($elapsed -lt $TimeoutSec) {
        $hb = (Get-VM -Name $VMName -ErrorAction SilentlyContinue).Heartbeat
        if ($hb -in @('OkApplicationsHealthy','OkApplicationsUnknown')) {
            Write-Step "  Heartbeat OK: $VMName (${elapsed}s)"; return $true
        }
        Start-Sleep -Seconds 5; $elapsed += 5
    }
    Write-Warning "Heartbeat timeout for $VMName after ${TimeoutSec}s"
    return $false
}

function Set-WindowsVMConfig {
    param(
        [Parameter(Mandatory)][string]$VMName,
        [Parameter(Mandatory)][PSCredential]$Cred,
        [Parameter(Mandatory)][hashtable]$Config
    )
    Invoke-Command -VMName $VMName -Credential $Cred -ScriptBlock {
        param($cfg)
        if ($cfg.IPAddress) {
            $adap = Get-NetAdapter | Where-Object Status -eq 'Up' | Select-Object -First 1
            if ($adap) {
                Get-NetIPAddress -InterfaceIndex $adap.ifIndex -AddressFamily IPv4 -ErrorAction SilentlyContinue |
                    Remove-NetIPAddress -Confirm:$false -ErrorAction SilentlyContinue
                Get-NetRoute -InterfaceIndex $adap.ifIndex -DestinationPrefix '0.0.0.0/0' -ErrorAction SilentlyContinue |
                    Remove-NetRoute -Confirm:$false -ErrorAction SilentlyContinue
                New-NetIPAddress -InterfaceIndex $adap.ifIndex -IPAddress $cfg.IPAddress -PrefixLength 24 `
                    -DefaultGateway $cfg.Gateway -ErrorAction SilentlyContinue
            }
        }
        if ($cfg.AdminPassword) {
            Set-LocalUser -Name 'Administrator' -Password (ConvertTo-SecureString $cfg.AdminPassword -AsPlainText -Force) -ErrorAction SilentlyContinue
        }
        if ($cfg.StudentUser -and $cfg.StudentPassword) {
            $sp = ConvertTo-SecureString $cfg.StudentPassword -AsPlainText -Force
            $u  = Get-LocalUser -Name $cfg.StudentUser -ErrorAction SilentlyContinue
            if (-not $u) {
                New-LocalUser -Name $cfg.StudentUser -Password $sp -FullName 'SOC Analyst' -PasswordNeverExpires -ErrorAction SilentlyContinue
            } else {
                Set-LocalUser -Name $cfg.StudentUser -Password $sp -ErrorAction SilentlyContinue
            }
            Add-LocalGroupMember -Group 'Administrators' -Member $cfg.StudentUser -ErrorAction SilentlyContinue
        }
        # Configure Splunk Universal Forwarder outputs.conf
        if ($cfg.SplunkForwarderTarget) {
            $outputsConf = @"
[tcpout]
defaultGroup = cyberlab_indexer

[tcpout:cyberlab_indexer]
server = $($cfg.SplunkForwarderTarget):9997
"@
            $ufPath = 'C:\Program Files\SplunkUniversalForwarder\etc\system\local'
            if (Test-Path $ufPath) {
                $outputsConf | Set-Content -Path "$ufPath\outputs.conf" -Encoding UTF8 -Force
                Restart-Service -Name SplunkForwarder -ErrorAction SilentlyContinue
            }
        }
        # Configure Windows audit policy for richer event logs
        auditpol /set /subcategory:"Logon" /success:enable /failure:enable 2>&1 | Out-Null
        auditpol /set /subcategory:"User Account Management" /success:enable /failure:enable 2>&1 | Out-Null
        auditpol /set /subcategory:"Process Creation" /success:enable /failure:enable 2>&1 | Out-Null
        # Enable PowerShell Script Block Logging
        $pshPath = 'HKLM:\SOFTWARE\Policies\Microsoft\Windows\PowerShell\ScriptBlockLogging'
        if (-not (Test-Path $pshPath)) { New-Item -Path $pshPath -Force | Out-Null }
        Set-ItemProperty -Path $pshPath -Name 'EnableScriptBlockLogging' -Value 1 -Type DWord -ErrorAction SilentlyContinue
    } -ArgumentList $Config -ErrorAction SilentlyContinue
}

function Set-LinuxVMConfig {
    param(
        [Parameter(Mandatory)][string]$TargetIp,
        [Parameter(Mandatory)][string]$User,
        [Parameter(Mandatory)][string]$Password,
        [string]$NewIp,
        [string]$Gateway,
        [string]$Hostname,
        [string[]]$ExtraCommands = @()
    )
    $allCmds = @()
    if ($Hostname) { $allCmds += "sudo hostnamectl set-hostname '${Hostname}'" }
    if ($NewIp -and $Gateway) {
        $yaml = "network:`n  version: 2`n  ethernets:`n    eth0:`n      addresses: [`"${NewIp}/24`"]`n      routes:`n        - to: default`n          via: ${Gateway}`n      nameservers:`n        addresses: [8.8.8.8]"
        $allCmds += "printf '${yaml}' | sudo tee /etc/netplan/50-lab.yaml && sudo netplan apply"
    }
    $allCmds += $ExtraCommands
    foreach ($cmd in $allCmds) {
        & sshpass -p $Password ssh -o StrictHostKeyChecking=no -o ConnectTimeout=15 "${User}@${TargetIp}" $cmd 2>&1 | Out-Null
    }
}

function Register-AttackSimulationTask {
    param(
        [Parameter(Mandatory)][string]$VMName,
        [Parameter(Mandatory)][PSCredential]$Cred
    )
    # Creates scheduled tasks that fire 30 min after deploy to generate suspicious events
    Invoke-Command -VMName $VMName -Credential $Cred -ScriptBlock {
        # Task 1: Simulate failed logons (Event 4625)
        $failedLogonScript = @'
$cred = New-Object PSCredential('fakeuser', (ConvertTo-SecureString 'WrongPass!' -AsPlainText -Force))
1..5 | ForEach-Object {
    Start-Process -FilePath 'net.exe' -ArgumentList 'use \\127.0.0.1\IPC$ /user:Administrator WrongPassword123' -WindowStyle Hidden -ErrorAction SilentlyContinue
}
'@
        $failedLogonScript | Set-Content -Path 'C:\Windows\Temp\sim_failed_logon.ps1' -Encoding UTF8 -Force

        # Task 2: Simulate PowerShell encoded command (Event 4104)
        $psEncodedPayload = [Convert]::ToBase64String([System.Text.Encoding]::Unicode.GetBytes('Write-EventLog -LogName Application -Source "CyberLab-Sim" -EventId 9999 -Message "Simulated encoded PS command" -EntryType Warning -ErrorAction SilentlyContinue'))
        $encodedScript = @"
try { [System.Diagnostics.EventLog]::CreateEventSource('CyberLab-Sim','Application') } catch {}
powershell.exe -EncodedCommand $psEncodedPayload
"@
        $encodedScript | Set-Content -Path 'C:\Windows\Temp\sim_encoded_ps.ps1' -Encoding UTF8 -Force

        # Task 3: Simulate new user account creation (Event 4720)
        $userCreationScript = @'
$tempUser = "svc_sim_$(Get-Random -Maximum 9999)"
New-LocalUser -Name $tempUser -NoPassword -ErrorAction SilentlyContinue
Start-Sleep -Seconds 5
Remove-LocalUser -Name $tempUser -ErrorAction SilentlyContinue
'@
        $userCreationScript | Set-Content -Path 'C:\Windows\Temp\sim_user_creation.ps1' -Encoding UTF8 -Force

        # Register tasks to fire 30 minutes from now
        $triggerTime = (Get-Date).AddMinutes(30)

        $simTasks = @(
            @{ Name='CyberLab-SimFailedLogon';  Script='C:\Windows\Temp\sim_failed_logon.ps1' }
            @{ Name='CyberLab-SimEncodedPS';    Script='C:\Windows\Temp\sim_encoded_ps.ps1' }
            @{ Name='CyberLab-SimUserCreation'; Script='C:\Windows\Temp\sim_user_creation.ps1' }
        )

        foreach ($task in $simTasks) {
            $existing = Get-ScheduledTask -TaskName $task.Name -ErrorAction SilentlyContinue
            if ($existing) { Unregister-ScheduledTask -TaskName $task.Name -Confirm:$false -ErrorAction SilentlyContinue }
            $action    = New-ScheduledTaskAction -Execute 'powershell.exe' -Argument "-ExecutionPolicy Bypass -NonInteractive -File `"$($task.Script)`""
            $trigger   = New-ScheduledTaskTrigger -Once -At $triggerTime
            $settings  = New-ScheduledTaskSettingsSet -AllowStartIfOnBatteries -DontStopIfGoingOnBatteries -ExecutionTimeLimit '00:05:00'
            $principal = New-ScheduledTaskPrincipal -UserId 'SYSTEM' -LogonType ServiceAccount -RunLevel Highest
            Register-ScheduledTask -TaskName $task.Name -Action $action -Trigger $trigger `
                -Settings $settings -Principal $principal -Force | Out-Null
        }
    } -ErrorAction SilentlyContinue
}

function Invoke-EnsureVMSwitch {
    param([Parameter(Mandatory)][string]$SwitchName, [string]$SwitchType = 'Private')
    if (-not (Get-VMSwitch -Name $SwitchName -ErrorAction SilentlyContinue)) {
        New-VMSwitch -Name $SwitchName -SwitchType $SwitchType -ErrorAction Stop | Out-Null
        $script:createdSwitches.Add($SwitchName)
        Write-Step "  Switch created: $SwitchName ($SwitchType)"
    }
}

function Remove-LabSessionResources {
    param([string]$Reason = 'Deployment failure')
    Write-Warning "ROLLBACK: $Reason"
    foreach ($vmName in $createdVMs) {
        try {
            Stop-VM -Name $vmName -TurnOff -Force -ErrorAction SilentlyContinue
            Get-VMSnapshot -VMName $vmName -ErrorAction SilentlyContinue | Remove-VMSnapshot -Confirm:$false -ErrorAction SilentlyContinue
            Remove-VM -Name $vmName -Force -ErrorAction SilentlyContinue
        } catch { Write-Warning "Could not remove VM $vmName : $_" }
    }
    $sessionDir = Join-Path $VMBasePath $SessionIdStr
    if (Test-Path $sessionDir) { Remove-Item -Path $sessionDir -Recurse -Force -ErrorAction SilentlyContinue }
    foreach ($sw in $createdSwitches) {
        try { Remove-VMSwitch -Name $sw -Force -ErrorAction SilentlyContinue } catch { }
    }
}

