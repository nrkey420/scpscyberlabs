# STEP 2: DEPLOY SHARED VMs
# ═══════════════════════════════════════════════════════════════════════════════
Write-Step "=== STEP 2: Deploying shared VMs ===" 'Cyan'

$sharedIpBase    = "10.${ClassId}.0"
$secOnionIp      = "${sharedIpBase}.50"
$splunkIp        = "${sharedIpBase}.51"
$soName          = "Lab03-SecOnion-C${ClassId}-${ShortId}"
$splunkName      = "Lab03-Splunk-C${ClassId}-${ShortId}"

$soAnalystPwd    = New-RandomPassword
$soInstrPwd      = New-RandomPassword
$splunkAdminPwd  = New-RandomPassword
$splunkInstrPwd  = New-RandomPassword

try {
    $sharedDir = Join-Path $sessionRoot 'shared'
    if (-not (Test-Path $sharedDir)) { New-Item -ItemType Directory -Path $sharedDir -Force | Out-Null }

    # Security Onion
    $soDisk = Join-Path $sharedDir "${soName}.vhdx"
    New-DifferencingDisk -ParentPath (Join-Path $TemplatePath $Templates.SecOnion) -ChildPath $soDisk
    New-LabVM -VMName $soName -VHDXPath $soDisk -MemoryMB 8192 -vCPU 4 `
              -SwitchNames @("shared-soc-net-C${ClassId}") | Out-Null
    $createdVMs.Add($soName)

    # Splunk
    $splunkDisk = Join-Path $sharedDir "${splunkName}.vhdx"
    New-DifferencingDisk -ParentPath (Join-Path $TemplatePath $Templates.Splunk) -ChildPath $splunkDisk
    New-LabVM -VMName $splunkName -VHDXPath $splunkDisk -MemoryMB 8192 -vCPU 4 `
              -SwitchNames @("shared-soc-net-C${ClassId}") | Out-Null
    $createdVMs.Add($splunkName)

    # Start shared VMs
    Write-Step "Starting shared VMs..."
    Start-VM -Name $soName -ErrorAction Stop
    Start-VM -Name $splunkName -ErrorAction Stop

    Wait-VMHeartbeat -VMName $soName     -TimeoutSec 300 | Out-Null
    Wait-VMHeartbeat -VMName $splunkName -TimeoutSec 300 | Out-Null
    Start-Sleep -Seconds 60  # Extra boot settling time for Security Onion

    # Configure Security Onion
    Write-Step "  Configuring Security Onion ($soName) at ${secOnionIp}..."
    $soSshReady = $false
    for ($a = 1; $a -le 12; $a++) {
        $r = & sshpass -p 'changeme' ssh -o StrictHostKeyChecking=no -o ConnectTimeout=10 "analyst@${secOnionIp}" 'echo ssh_ok' 2>&1
        if ($r -match 'ssh_ok') { $soSshReady = $true; break }
        Start-Sleep -Seconds 15
    }

    if ($soSshReady) {
        $soCmds = @(
            "echo 'analyst:${soAnalystPwd}' | sudo chpasswd",
            "sudo useradd -m -s /bin/bash instructor && echo 'instructor:${soInstrPwd}' | sudo chpasswd && sudo usermod -aG sudo instructor",
            # Enable syslog reception on port 514 UDP for Linux VM forwarding
            "sudo so-allow syslog 0.0.0.0/0 2>/dev/null || true",
            # Configure elastic index pattern initialization
            "sudo so-elasticsearch-query PUT 'cyberlab_index' --json '{\"settings\":{\"number_of_shards\":1}}' 2>/dev/null || true"
        )
        foreach ($cmd in $soCmds) {
            & sshpass -p 'changeme' ssh -o StrictHostKeyChecking=no -o ConnectTimeout=30 "analyst@${secOnionIp}" $cmd 2>&1 | Out-Null
        }
    }

    # Configure Splunk
    Write-Step "  Configuring Splunk ($splunkName) at ${splunkIp}..."
    $splunkSshReady = $false
    for ($a = 1; $a -le 12; $a++) {
        $r = & sshpass -p 'changeme' ssh -o StrictHostKeyChecking=no -o ConnectTimeout=10 "splunkadmin@${splunkIp}" 'echo ssh_ok' 2>&1
        if ($r -match 'ssh_ok') { $splunkSshReady = $true; break }
        Start-Sleep -Seconds 15
    }

    if ($splunkSshReady) {
        $splunkCmds = @(
            # Set admin password
            "/opt/splunk/bin/splunk edit user admin -password '${splunkAdminPwd}' -auth admin:changeme --accept-license --answer-yes 2>/dev/null || true",
            # Create instructor user
            "/opt/splunk/bin/splunk add user instructor -password '${splunkInstrPwd}' -role admin -auth admin:${splunkAdminPwd} 2>/dev/null || true",
            # Enable receiving on port 9997 for Splunk UF
            "/opt/splunk/bin/splunk enable listen 9997 -auth admin:${splunkAdminPwd} 2>/dev/null || true",
            # Create cyberlab index
            "/opt/splunk/bin/splunk add index cyberlab -auth admin:${splunkAdminPwd} 2>/dev/null || true",
            # Create windows index (may already exist in template)
            "/opt/splunk/bin/splunk add index windows -auth admin:${splunkAdminPwd} 2>/dev/null || true",
            # Restart Splunk to apply
            "/opt/splunk/bin/splunk restart --accept-license --answer-yes 2>/dev/null || true"
        )
        foreach ($cmd in $splunkCmds) {
            & sshpass -p 'changeme' ssh -o StrictHostKeyChecking=no -o ConnectTimeout=60 "splunkadmin@${splunkIp}" $cmd 2>&1 | Out-Null
        }
    }

    Checkpoint-VM -Name $soName     -SnapshotName 'InitialState' -ErrorAction SilentlyContinue
    Checkpoint-VM -Name $splunkName -SnapshotName 'InitialState' -ErrorAction SilentlyContinue

    $sessionCredentials['shared'] = @{
        SecurityOnion = @{
            VMName    = $soName
            IPAddress = $secOnionIp
            analyst    = @{ Username = 'analyst';    Password = $soAnalystPwd }
            instructor = @{ Username = 'instructor'; Password = $soInstrPwd }
            WebUI      = "https://${secOnionIp}"
        }
        Splunk = @{
            VMName    = $splunkName
            IPAddress = $splunkIp
            admin      = @{ Username = 'admin';      Password = $splunkAdminPwd }
            instructor = @{ Username = 'instructor'; Password = $splunkInstrPwd }
            WebUI      = "https://${splunkIp}:8000"
        }
    }

    Write-Step "Shared VMs ready." 'Green'
} catch {
    Remove-LabSessionResources -Reason "Shared VM deployment failed: $_"
    Stop-Transcript; throw
}

